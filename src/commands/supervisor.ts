export function registerSupervisorCommand(program: Command, emit: EmitContext): void {
  const registry = createBuiltinHarnessRegistry();
  const supervisor = program
    .command("supervisor")
    .description("Run a bounded specialist team over a durable-work dependency graph.");

  supervisor
    .command("create [root-work-id]")
    .description("Freeze an existing-root goal or an objective-first bounded mission.")
    .requiredOption("--team <file>", "JSON object mapping specialist ids to profiles")
    .option("--id <goal-id>", "Stable goal id (generated by default)")
    .option("--title <text>", "Goal title (defaults to root work title)")
    .option("--max-cycles <n>", "Foreground cycle ceiling (default 50)")
    .option("--max-runtime-ms <n>", "Foreground wall-time ceiling (default 14400000)")
    .option("--max-parallel-work <n>", "Concurrent work-item ceiling (default 1)")
    .option("--max-total-attempts <n>", "Graph-wide attempt ceiling (default 100)")
    .option("--max-agents-per-work <n>", "Child-agent ceiling per work item (default 20)")
    .option("--agent-concurrency <n>", "Child concurrency per work item (default 4)")
    .option("--accept-passing-proof", "Allow the supervisor to explicitly accept passing proof")
    .option("--no-resume-approved", "Stop after an approval instead of resuming its parked run")
    .option("--retry-blocked", "Allow bounded retry of blocked work")
    .option("--replanning <file>", "Frozen planner policy and allowed workflow-template catalog")
    .option("--mission <file>", "Frozen objective, acceptance criteria, and milestone bound")
    .option("--json", "Emit the complete supervisor record as JSON")
    .action((rootWorkId: string | undefined, opts: CreateOpts) => {
      withSupervisorRoot(emit, (coordRoot) => {
        const specialists = readTeamFile(opts.team);
        for (const [id, profile] of Object.entries(specialists)) {
          if (profile.harness && !registry.get(profile.harness)) {
            throw new Error(
              `specialist ${id} names unknown harness ${JSON.stringify(profile.harness)}`,
            );
          }
        }
        emitSupervisor(
          createSupervisor({
            coordRoot,
            rootWorkId,
            specialists,
            id: opts.id,
            title: opts.title,
            limits: {
              max_cycles: integer(opts.maxCycles),
              max_runtime_ms: integer(opts.maxRuntimeMs),
              max_parallel_work: integer(opts.maxParallelWork),
              max_total_attempts: integer(opts.maxTotalAttempts),
              max_agents_per_work: integer(opts.maxAgentsPerWork),
              agent_concurrency: integer(opts.agentConcurrency),
            },
            automation: {
              accept_passing_proof: opts.acceptPassingProof,
              resume_approved: opts.resumeApproved,
              retry_blocked: opts.retryBlocked,
            },
            mission: opts.mission ? readMissionFile(opts.mission) : undefined,
            replanning: opts.replanning ? readReplanningFile(opts.replanning) : undefined,
          }),
          opts.json,
          emit,
          true,
        );
      });
    });

  supervisor
    .command("list")
    .description("List durable goals and their derived next action.")
    .option("--json", "Emit complete supervisor records as JSON")
    .action((opts: { json?: boolean }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        const records = listSupervisors(coordRoot);
        if (opts.json) {
          emit.config({ format: "json" });
          emit.data(records);
        } else if (records.length === 0) {
          emit.text("no durable supervisors\n");
        } else {
          emit.text(`${records.map(renderSupervisorRow).join("\n")}\n`);
        }
      });
    });

  supervisor
    .command("show <goal-id>")
    .description("Show one goal, frozen team, bounds, graph state, and next action.")
    .option("--json", "Emit the complete supervisor record as JSON")
    .action((goalId: string, opts: { json?: boolean }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        emitSupervisor(readSupervisor(coordRoot, goalId), opts.json, emit, true);
      });
    });

  registerPlanCommand(supervisor, emit);
  registerServiceCommand(supervisor, registry, emit);
  registerRunCommand(supervisor, "tick", registry, emit);
  registerRunCommand(supervisor, "run", registry, emit);
}

function registerPlanCommand(supervisor: Command, emit: EmitContext): void {
  const plan = supervisor
    .command("plan")
    .description("Inspect and resolve bounded planner proposals for a durable goal.");

  plan
    .command("list <goal-id>")
    .description("List append-only replanning attempts for one goal.")
    .option("--json", "Emit complete plan records as JSON")
    .action((goalId: string, opts: { json?: boolean }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        const plans = readSupervisor(coordRoot, goalId).plans;
        if (opts.json) {
          emit.config({ format: "json" });
          emit.data(plans);
        } else if (plans.length === 0) {
          emit.text(`supervisor ${goalId} has no replanning attempts\n`);
        } else {
          emit.text(`${plans.map(renderPlanRow).join("\n")}\n`);
        }
      });
    });

  plan
    .command("show <goal-id> <plan-id>")
    .description("Show one planner request, schema-gated proposal, and audit events.")
    .option("--json", "Emit the complete plan record as JSON")
    .action((goalId: string, planId: string, opts: { json?: boolean }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        emitPlan(readSupervisorPlan(coordRoot, goalId, planId), opts.json, emit);
      });
    });

  plan
    .command("approve <goal-id> <plan-id>")
    .description("Materialize a reviewed proposal and advance the active immutable root.")
    .option("--actor <name>", "Actor recorded on the plan decision")
    .option("--reason <text>", "Reason for approval")
    .option("--json", "Emit the plan outcome as JSON")
    .action(
      (
        goalId: string,
        planId: string,
        opts: { actor?: string; reason?: string; json?: boolean },
      ) => {
        withSupervisorRoot(emit, (coordRoot) => {
          emitPlanOutcome(
            approveSupervisorPlan({
              coordRoot,
              goalId,
              planId,
              actor: opts.actor,
              reason: opts.reason,
            }),
            opts.json,
            emit,
          );
        });
      },
    );

  plan
    .command("reject <goal-id> <plan-id>")
    .description("Reject a pending proposal without mutating durable work.")
    .requiredOption("--reason <text>", "Reason the proposal is rejected")
    .option("--actor <name>", "Actor recorded on the plan decision")
    .option("--json", "Emit the plan outcome as JSON")
    .action(
      (
        goalId: string,
        planId: string,
        opts: { actor?: string; reason: string; json?: boolean },
      ) => {
        withSupervisorRoot(emit, (coordRoot) => {
          emitPlanOutcome(
            rejectSupervisorPlan({
              coordRoot,
              goalId,
              planId,
              actor: opts.actor,
              reason: opts.reason,
            }),
            opts.json,
            emit,
          );
        });
      },
    );
}

function registerServiceCommand(
  supervisor: Command,
  registry: ReturnType<typeof createBuiltinHarnessRegistry>,
  emit: EmitContext,
): void {
  const service = supervisor
    .command("service")
    .description("Run explicitly enrolled durable goals in a restartable background service.");

  addServiceOptions(
    service
      .command("start [goal-ids...]")
      .description("Configure and start the detached per-repository supervisor service."),
  )
    .option("--json", "Emit complete service status as JSON")
    .action(async (goalIds: string[], opts: ServiceOpts) => {
      await withSupervisorRootAsync(emit, async (coordRoot) => {
        prepareServiceConfig(coordRoot, goalIds, opts, registry);
        const status = await spawnSupervisorService(coordRoot);
        emitServiceStatus(status, opts.json, emit);
      });
    });

  addServiceOptions(
    service
      .command("run [goal-ids...]")
      .description("Run the persistent service loop in the foreground for a process manager."),
  )
    .option("--json", "Emit terminal service status as JSON")
    .action(async (goalIds: string[], opts: ServiceOpts) => {
      await withSupervisorRootAsync(emit, async (coordRoot) => {
        const config = prepareServiceConfig(coordRoot, goalIds, opts, registry);
        const status = await runSupervisorServiceDaemon({
          coordRoot,
          engine: serviceEngine(config, registry),
          onLog: opts.json ? undefined : (line) => emit.text(`${line}\n`),
        });
        if (opts.json) {
          emit.config({ format: "json" });
          emit.data(status);
        }
      });
    });

  service
    .command("status")
    .description("Show service liveness, heartbeat, enrolled goals, and durable wake state.")
    .option("--json", "Emit complete service status as JSON")
    .action((opts: { json?: boolean }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        emitServiceStatus(readSupervisorServiceStatus(coordRoot), opts.json, emit);
      });
    });

  service
    .command("stop")
    .description("Request a graceful stop; an active goal tick is allowed to finish safely.")
    .option("--json", "Emit complete service status as JSON")
    .action(async (opts: { json?: boolean }) => {
      await withSupervisorRootAsync(emit, async (coordRoot) => {
        let status = requestSupervisorServiceStop(coordRoot);
        const deadline = Date.now() + 5_000;
        while (status.running && Date.now() < deadline) {
          await delay(50);
          status = readSupervisorServiceStatus(coordRoot);
        }
        emitServiceStatus(status, opts.json, emit);
      });
    });

  service
    .command("logs")
    .description("Show the tail of the private background-service log.")
    .option("--lines <n>", "Number of lines to show (default 50)", "50")
    .action((opts: { lines: string }) => {
      withSupervisorRoot(emit, (coordRoot) => {
        const lines = integer(opts.lines) ?? 50;
        if (lines > 10_000) throw new Error("service log lines must not exceed 10000");
        const path = supervisorServiceLogPath(coordRoot);
        if (!existsSync(path)) {
          emit.text("no supervisor service log\n");
          return;
        }
        const selected = readFileSync(path, "utf8")
          .split("\n")
          .slice(-(lines + 1))
          .join("\n");
        emit.text(selected.endsWith("\n") ? selected : `${selected}\n`);
      });
    });

  service
    .command("daemon", { hidden: true })
    .description("Internal detached service entrypoint.")
    .action(async () => {
      await withSupervisorRootAsync(emit, async (coordRoot) => {
        const config = readSupervisorServiceConfig(coordRoot);
        await runSupervisorServiceDaemon({
          coordRoot,
          engine: serviceEngine(config, registry),
          onLog: (line) => emit.text(`${line}\n`),
        });
      });
    });
}

function addServiceOptions(command: Command): Command {
  return command
    .option("--wake-interval-ms <n>", "State-change poll interval (default 5000)")
    .option("--heartbeat-interval-ms <n>", "Heartbeat interval (default 2000)")
    .option("--error-backoff-base-ms <n>", "Initial service-error backoff (default 2000)")
    .option("--error-backoff-max-ms <n>", "Maximum service-error backoff (default 300000)")
    .option("--harness <name>", "Fallback harness for agent calls without a specialist")
    .option("--cwd <dir>", "Working directory for child agents")
    .option("--subscription-only", "Require stored harness-login billing")
    .option("--allow-api-billing", "Permit API-key override billing")
    .option("--policy <file>", "Host policy JSON/JSONC")
    .option("--isolation <mode>", "shared | worktree | sandbox | remote")
    .option("--approval-to <address>", "Address durable ASK requests");
}

function prepareServiceConfig(
  coordRoot: string,
  goalIds: string[],
  opts: ServiceOpts,
  registry: ReturnType<typeof createBuiltinHarnessRegistry>,
): SupervisorServiceConfig {
  if (goalIds.length === 0) {
    if (hasServiceConfigOverrides(opts)) {
      throw new Error("goal ids are required when changing supervisor service options");
    }
    return readSupervisorServiceConfig(coordRoot);
  }
  if (opts.harness && !registry.get(opts.harness)) {
    throw new Error(`unknown harness ${JSON.stringify(opts.harness)}`);
  }
  const policy = opts.policy ? loadPolicyFile(opts.policy) : undefined;
  return configureSupervisorService({
    coordRoot,
    goalIds,
    wakeIntervalMs: integer(opts.wakeIntervalMs),
    heartbeatIntervalMs: integer(opts.heartbeatIntervalMs),
    errorBackoffBaseMs: integer(opts.errorBackoffBaseMs),
    errorBackoffMaxMs: integer(opts.errorBackoffMaxMs),
    engine: {
      default_harness: opts.harness,
      cwd: opts.cwd,
      subscription_only:
        opts.subscriptionOnly === true ? true : workflowSubscriptionOnly(coordRoot),
      allow_api_billing: opts.allowApiBilling,
      policy,
      isolation: opts.isolation,
      approval_addressee: opts.approvalTo,
    },
  });
}

function hasServiceConfigOverrides(opts: ServiceOpts): boolean {
  return Boolean(
    opts.wakeIntervalMs ||
      opts.heartbeatIntervalMs ||
      opts.errorBackoffBaseMs ||
      opts.errorBackoffMaxMs ||
      opts.harness ||
      opts.cwd ||
      opts.subscriptionOnly ||
      opts.allowApiBilling ||
      opts.policy ||
      opts.isolation ||
      opts.approvalTo,
  );
}

function serviceEngine(
  config: SupervisorServiceConfig,
  registry: ReturnType<typeof createBuiltinHarnessRegistry>,
) {
  return {
    spawners: registry.spawners(),
    defaultHarness: config.engine.default_harness,
    cwd: config.engine.cwd,
    subscriptionOnly: config.engine.subscription_only,
    allowApiBilling: config.engine.allow_api_billing,
    harnessEvidence: Object.fromEntries(
      registry
        .list()
        .map((adapter) => [
          adapter.profile.id,
          { toolEvidence: adapter.profile.capabilities.toolEvidence },
        ]),
    ),
    policy: config.engine.policy,
    approvalMode: "park" as const,
    approvalAddressee: config.engine.approval_addressee,
    isolation: config.engine.isolation,
    networkAccess: "enabled" as const,
  };
}

function emitServiceStatus(
  status: SupervisorServiceStatus,
  json: boolean | undefined,
  emit: EmitContext,
): void {
  if (json) {
    emit.config({ format: "json" });
    emit.data(status);
    return;
  }
  if (!status.config && !status.record) {
    emit.text("supervisor service: unconfigured\n");
    return;
  }
  const state = status.running
    ? (status.record?.state ?? "running")
    : status.stale
      ? "stale"
      : "stopped";
  const lines = [
    `supervisor service: ${state}`,
    `goals: ${status.config?.goal_ids.join(", ") || "none"}`,
  ];
  if (status.record) {
    lines.push(`pid: ${status.record.pid} on ${status.record.host}`);
    lines.push(`heartbeat: ${status.record.heartbeat_at}`);
    lines.push(`sweeps: ${status.record.sweep_count}`);
    if (status.record.active_goal_id) lines.push(`active: ${status.record.active_goal_id}`);
    if (status.record.next_wake_at) lines.push(`next wake: ${status.record.next_wake_at}`);
    if (status.record.last_error) lines.push(`error: ${status.record.last_error}`);
  }
  const backingOff = Object.entries(status.runtime?.goals ?? {}).filter(
    ([, runtime]) => runtime.state === "backoff",
  );
  if (backingOff.length) {
    lines.push(`backoff: ${backingOff.map(([goalId]) => goalId).join(", ")}`);
  }
  emit.text(`${lines.join("\n")}\n`);
}

function registerRunCommand(
  supervisor: Command,
  mode: "tick" | "run",
  registry: ReturnType<typeof createBuiltinHarnessRegistry>,
  emit: EmitContext,
): void {
  supervisor
    .command(`${mode} <goal-id>`)
    .description(
      mode === "tick"
        ? "Perform at most one bounded scheduling cycle."
        : "Run bounded cycles until success, attention, no progress, or budget exhaustion.",
    )
    .option("--harness <name>", "Fallback harness for agent calls without a specialist")
    .option("--cwd <dir>", "Working directory for child agents")
    .option("--subscription-only", "Require stored harness-login billing")
    .option("--allow-api-billing", "Permit API-key override billing")
    .option("--policy <file>", "Host policy JSON/JSONC")
    .option("--isolation <mode>", "shared | worktree | sandbox | remote")
    .option("--approval-to <address>", "Address durable ASK requests")
    .option("--actor <name>", "Actor recorded on supervisor work decisions")
    .option("--json", "Emit the complete run report as JSON")
    .action(async (goalId: string, opts: RunOpts) => {
      await withSupervisorRootAsync(emit, async (coordRoot) => {
        if (opts.harness && !registry.get(opts.harness)) {
          throw new Error(`unknown harness ${JSON.stringify(opts.harness)}`);
        }
        const report = await runSupervisor({
          coordRoot,
          goalId,
          mode,
          actor: opts.actor,
          onLog: opts.json ? undefined : (line) => emit.text(`${line}\n`),
          engine: {
            spawners: registry.spawners(),
            defaultHarness: opts.harness,
            cwd: opts.cwd,
            subscriptionOnly:
              opts.subscriptionOnly === true ? true : workflowSubscriptionOnly(coordRoot),
            allowApiBilling: opts.allowApiBilling,
            harnessEvidence: Object.fromEntries(
              registry
                .list()
                .map((adapter) => [
                  adapter.profile.id,
                  { toolEvidence: adapter.profile.capabilities.toolEvidence },
                ]),
            ),
            policy: opts.policy ? loadPolicyFile(opts.policy) : undefined,
            approvalMode: "park",
            approvalAddressee: opts.approvalTo,
            isolation: opts.isolation,
            networkAccess: "enabled",
          },
        });
        emitSupervisorReport(report, opts.json, emit);
      });
    });
}

function readTeamFile(path: string): Record<string, WorkflowSpecialistProfile> {
  const absolute = resolve(path);
  let value: unknown;
  try {
    value = JSON.parse(readFileSync(absolute, "utf8"));
  } catch (error) {
    throw new Error(`cannot read supervisor team at ${absolute}: ${(error as Error).message}`);
  }
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error("supervisor team must be a JSON object keyed by specialist id");
  }
  return value as Record<string, WorkflowSpecialistProfile>;
}

function readReplanningFile(path: string): CreateSupervisorReplanningInput {
  const absolute = resolve(path);
  let value: unknown;
  try {
    value = JSON.parse(readFileSync(absolute, "utf8"));
  } catch (error) {
    throw new Error(
      `cannot read supervisor replanning policy at ${absolute}: ${(error as Error).message}`,
    );
  }
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error("supervisor replanning policy must be a JSON object");
  }
  const config = value as Record<string, unknown>;
  if (typeof config.planner_specialist !== "string") {
    throw new Error("supervisor replanning planner_specialist must be a string");
  }
  if (
    !config.templates ||
    typeof config.templates !== "object" ||
    Array.isArray(config.templates)
  ) {
    throw new Error("supervisor replanning templates must be an object");
  }
  const base = dirname(absolute);
  const templates = Object.fromEntries(
    Object.entries(config.templates as Record<string, unknown>).map(([id, raw]) => {
      if (!raw || typeof raw !== "object" || Array.isArray(raw)) {
        throw new Error(`supervisor replanning template ${id} must be an object`);
      }
      const template = raw as Record<string, unknown>;
      if (typeof template.workflow !== "string") {
        throw new Error(`supervisor replanning template ${id} workflow must be a string`);
      }
      return [
        id,
        {
          workflowPath: isAbsolute(template.workflow)
            ? template.workflow
            : resolve(base, template.workflow),
          maxAttempts: jsonInteger(template.max_attempts, `template ${id} max_attempts`),
          root: jsonBoolean(template.root, `template ${id} root`),
        },
      ];
    }),
  );
  return {
    plannerSpecialist: config.planner_specialist,
    autoApply: jsonBoolean(config.auto_apply, "replanning auto_apply"),
    maxReplans: jsonInteger(config.max_replans, "replanning max_replans"),
    maxWorkItemsPerPlan: jsonInteger(
      config.max_work_items_per_plan,
      "replanning max_work_items_per_plan",
    ),
    maxTotalWorkItems: jsonInteger(config.max_total_work_items, "replanning max_total_work_items"),
    templates,
  };
}

function readMissionFile(path: string): CreateSupervisorMissionInput {
  const absolute = resolve(path);
  let value: unknown;
  try {
    value = JSON.parse(readFileSync(absolute, "utf8"));
  } catch (error) {
    throw new Error(`cannot read supervisor mission at ${absolute}: ${(error as Error).message}`);
  }
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error("supervisor mission must be a JSON object");
  }
  const config = value as Record<string, unknown>;
  if (typeof config.objective !== "string") {
    throw new Error("supervisor mission objective must be a string");
  }
  if (
    !Array.isArray(config.acceptance) ||
    config.acceptance.some((item) => typeof item !== "string")
  ) {
    throw new Error("supervisor mission acceptance must be an array of strings");
  }
  return {
    objective: config.objective,
    acceptance: config.acceptance as string[],
    maxMilestones: jsonInteger(config.max_milestones, "mission max_milestones"),
  };
}

function emitSupervisor(
  record: SupervisorRecord,
  json: boolean | undefined,
  emit: EmitContext,
  detail = false,
): void {
  if (json) {
    emit.config({ format: "json" });
    emit.data(record);
    return;
  }
  if (!detail) {
    emit.text(`${renderSupervisorRow(record)}\n`);
    return;
  }
  const projection = record.projection;
  const lines = [
    `${record.intent.id}: ${record.intent.title}`,
    `state: ${projection.state}`,
    `root: ${projection.root_materialized ? projection.root_work_id : "pending initial plan"}`,
    `reason: ${projection.reason}`,
    `next: ${projection.next_action}`,
    `work: ${projection.work_ids.length}`,
    `attempts: ${projection.attempts_used}/${record.intent.limits.max_total_attempts}`,
    `specialists: ${projection.specialists.join(", ") || "none"}`,
    `automation: accept=${record.intent.automation.accept_passing_proof}, ` +
      `resume=${record.intent.automation.resume_approved}, retry=${record.intent.automation.retry_blocked}`,
  ];
  if (record.intent.replanning) {
    lines.push(
      `replanning: generation=${projection.plan_generation}, used=${projection.replans_used}/${record.intent.replanning.max_replans}, auto_apply=${record.intent.replanning.auto_apply}`,
    );
    if (projection.root_work_id !== record.intent.root_work_id) {
      lines.push(`original root: ${record.intent.root_work_id}`);
    }
    if (projection.pending_plan_id) lines.push(`pending plan: ${projection.pending_plan_id}`);
  }
  if (record.intent.mission) {
    lines.push(`mission: ${record.intent.mission.objective}`);
    lines.push(
      `milestones: ${projection.milestones_completed}/${record.intent.mission.max_milestones}`,
    );
    lines.push(`mission acceptance: ${record.intent.mission.acceptance.length}`);
  }
  if (projection.attention_work.length) {
    lines.push(`attention: ${projection.attention_work.join(", ")}`);
  }
  emit.text(`${lines.join("\n")}\n`);
}

